import java.util.*;
import java.lang.*;
import java.io.*;

/* Name of the class has to be "Main" only if the class is public. */
class CodeChef
{
	public static void main (String[] args) throws java.lang.Exception
	{
	    try{
		    Scanner s=new Scanner(System.in);
		    int a=s.nextInt();
		    int b=s.nextInt();
		    for(int i=a;i<=b;i++){
		        if(i%2!=0){
		            System.out.print(i+" ");
		        }
		    }
		    
		}catch(Exception e){
	        
	    }
	}
}
